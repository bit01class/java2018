class Ex29{

public static void main(String[] args){
// 1~10까지 출력하시오
	for(int i=0; i<10; i++){
		System.out.print(i+1+" ");
	}

	System.out.println();

	System.out.println("-----------------------------");
	System.out.println();
	int i=0;
	while(i<10){
		System.out.print(i+1+" ");
		i++;
	}

	System.out.println();
}

}



