class Ex04{

	public static void main(String[] args){
		/*주석 - 주석은 프로그래머가 메모합니다
		 <- 다음에 내용은 주석으로 인정
		*/
		System.out.println("사칙연산(+-*/)"); // 주석..
		System.out.println(5+2);//합
		System.out.println(5-2);//차
		System.out.println(5*2);//곱
		System.out.println(5/2);//나누기(몫)
		System.out.println(5%2);//나누기(나머지)
		System.out.println("------------------");
		System.out.println(5.0+2.0);
		System.out.println(5.0-2.0);
		System.out.println(5.0*2.0);
		System.out.println(5.0/2.0);
		System.out.println(5.0%2.0);
		System.out.println("------------------");
		System.out.println((5*100+2*100)/100.0);
		System.out.println((5*100-2*100)/100.0);
		System.out.println((5*100*2)/100.0);
		System.out.println((5*100/2)/100.0);
		System.out.println((5*100%2)/100.0);
		System.out.println("------------------");
		System.out.println((1+2)*3+(1*2)-(3*1));
	}


}














