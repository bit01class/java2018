class Ex11{

public static void main(String[] args){
	// 자료형
	// 정수형(기본: int)
	// byte short int long
	// byte(1byte=8bit=2^8=256)
	byte su1;
	su1=126;
	su1++;
	su1++;
	System.out.println("su1="+su1);
	System.out.println("--------------------");
	// short(2byte=16bit=2^16=65536)
	short su2=32766;
	su2++;
	su2++;
	System.out.println("su2="+su2);
	System.out.println("--------------------");
	long su3;
	su3=9223372036854775807L;
	su3++;
	System.out.println("su3="+su3);
}

}















